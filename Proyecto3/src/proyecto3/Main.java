    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String from = ComboPrecio.getSelectedItem().toString();
        String to = ComboPrecio1.getSelectedItem().toString();
        Dijkstra dijkstra = new Dijkstra(Dijkstra.Element.EDGE, null, "Costo");
        dijkstra.init(grafo);
        dijkstra.setSource(grafo.getNode(from));
        dijkstra.compute();
        Graph temp = new MultiGraph("Costo", false, true);
        for (Node node : dijkstra.getPathNodes(grafo.getNode(to))) {
            temp.addNode(node.getId());
        }
        // Color in red all the edges in the shortest path tree
        for (Edge edge : dijkstra.getPathEdges(grafo.getNode(to))) {
            temp.addEdge(edge.getId(), edge.getNode0().getId(), edge.getNode1().getId(), true).addAttribute("Costo", edge.getAttribute("Costo").toString());
        }
        for (Node node : temp) {
            node.addAttribute("ui.label", node.getId());
        }
        for (Edge e : temp.getEachEdge()) {
            e.addAttribute("ui.label", "Costo: "+e.getNumber("Costo"));
        }
        Viewer viewer = temp.display();
        viewer.setCloseFramePolicy(Viewer.CloseFramePolicy.CLOSE_VIEWER);
        // cleanup to save memory if solutions are no longer needed
        dijkstra.clear();
    }//GEN-LAST:event_jButton1ActionPerformed

