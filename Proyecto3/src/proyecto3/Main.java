    private void JB_lecturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JB_lecturaActionPerformed
        JFileChooser chooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter(
                "Archivo de Texto", "txt");
        chooser.setFileFilter(filter);
        int returnVal = chooser.showOpenDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            try {
                FileReader f = new FileReader(chooser.getSelectedFile());
                BufferedReader b = new BufferedReader(f);
                String cadena;
                while ((cadena = b.readLine()) != null) {
                    String[] partes = cadena.split(",");
                    grafo.addEdge(partes[0] + " " + partes[1], partes[0], partes[1], true).addAttribute("Aerolinea", partes[2]);
                    grafo.getEdge(partes[0] + " " + partes[1]).addAttribute("Distancia", Double.parseDouble(partes[4]));
                    grafo.getEdge(partes[0] + " " + partes[1]).addAttribute("Costo", Double.parseDouble(partes[3]));
                }
                for (Node node : grafo) {
                    node.addAttribute("ui.label", node.getId());
                }
                for (Edge e : grafo.getEachEdge()) {
                    e.addAttribute("ui.label", "Distancia: " + e.getNumber("Distancia") + "Aerolinea: " + e.getLabel("Aerolinea") + "Precio: " + e.getNumber("Costo"));
                }
                b.close();
            } catch (Exception e) {
            }
        }
        jButton2.setEnabled(true);
        jButton3.setEnabled(true);
        jButton4.setEnabled(true);
    }//GEN-LAST:event_JB_lecturaActionPerformed

